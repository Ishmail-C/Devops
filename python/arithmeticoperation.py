'''
+   Addition
-   Subtraction
*   Multiplication
/   Division
%   Modulus
**  Exponentiation
//  Floor division
'''

print("\n\n__________Arithmetic Operations__________")
a = int(input("\nEnter \"a\" Value : "))
b = int(input("Enter \"b\" Value : "))
print("\n\n-----------Input values-----------------")
print("\ta=", a)
print("\tb=", b)
print("----------------------------------------")
print("\n\nAddition         [a+b]   : ", a+b)
print("Subtraction      [a-b]   : ", a-b)
print("Multiplication   [a*b]   : ", a*b)
print("Division         [a/b]   : ", a/b)
print("Floor division   [a//b]  : ", a//b)
print("Modulus          [a%b]   : ", a % b)
print("Exponentiation   [2^4]   : ", a**b)
print("\n\n__________________________________________")
