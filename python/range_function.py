'''
Range function in python

1-5 --> 1,2,3,4,5  #Sequence number
1-5 --> 2,4 +2  #Even number
range(5) --> 0,1,2,3,4  # n-1
range(2,5) --> 2,3,4  #n-1
'''
print(list(range(5)))  # Sequence numbers
print(list(range(2, 5)))  # n-1
print(list(range(0, 20, 2)))  # Print all even numbers in range
print(list(range(1, 20, 2)))  # Print all odd numbers in range
